segunda_menor(Raiz, Nota) :-
    nota(Raiz),
    semitono(Raiz, Nota).

segunda_mayor(Raiz, Nota) :-
    nota(Raiz),
    tono(Raiz, Nota).

tercera_menor(Raiz, Nota) :-
    nota(Raiz),
    tono(Raiz, SegundaMayor),
    semitono(SegundaMayor, Nota).

